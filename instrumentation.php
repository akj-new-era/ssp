<p align="justify">The <b>Instrumentation Subsystem</b> acquires data of interest from the environment through sensors and sends it in the required form to other subsystems for use, for example, the measurement of the magnetic field of the Earth. But for the sensors to provide and store correct data, they need to be calibrated and tested for the conditions they will face in space.
The Instrumentation subsystem has the following main tasks:
</p>
<!-- <p align="justify">  -->
<ol style="font-size:18px";>
<li><b>Sensor Selection</b>: The appropriate sensor needs to be selected considering the payload and various factors like space, power, etc.
<!-- <br><br> -->
<li><b>Sensor Interfacing</b>: The data that the sensor has recorded needs to be imported into a readable form and then interpreted.
<!-- <br><br> -->
<li><b>Sensor Calibration</b>: The main aim of calibration is to eliminate or minimize factors that cause inaccurate measurements. Calibration is usually done by establishing a relationship between a measurement taken using the instrument and the values already known to us.
<!-- <br><br> -->
<li><b>Data processing</b>: Sensor output has a variety of noises or fluctuations which need to be removed either by averaging the whole data or through some other method. The data also needs to be analyzed or filtered to obtain the desired output.
<!-- <br><br> -->
<li><b>Testing</b>: A replica of the conditions in space also needs to be built to test the functioning of the sensors.
</ol>
